    private void addjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addjButtonActionPerformed
        // TODO add your handling code here:
        if (testnamejTextField.getText().trim().isEmpty() || testDescjTextArea.getText().trim().isEmpty() || costjTextField.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please provide input for all fields", "Incomplete Form", JOptionPane.INFORMATION_MESSAGE);
        } else {
            LabTest test = enterprise.getLabTestDirectory().addLabTest();
            test.setName(testnamejTextField.getText());
            test.setDescription(testDescjTextArea.getText());
            test.setPrice(Integer.parseInt(costjTextField.getText()));
            JOptionPane.showMessageDialog(null, "Test Added Successfully!", "Test entry Created", JOptionPane.INFORMATION_MESSAGE);
            popData();
        }
    }//GEN-LAST:event_addjButtonActionPerformed

