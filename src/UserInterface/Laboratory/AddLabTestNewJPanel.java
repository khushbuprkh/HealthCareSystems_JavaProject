    private void testnamejTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_testnamejTextFieldKeyTyped
        // TODO add your handling code here:
        char a = evt.getKeyChar();
        if (!(Character.isAlphabetic(a) || Character.isDigit(a) || a == KeyEvent.VK_DELETE || a == KeyEvent.VK_BACK_SPACE)) {
            testnamejLabel.setVisible(true);
            evt.consume();
        }
    }//GEN-LAST:event_testnamejTextFieldKeyTyped

